package sbp.dto.enums;

public enum TransferStatus {
  // Начальные стадии
  PENDING, // Перевод создан, ожидает обработки (например, резервирования средств)
  AWAITING_CONFIRMATION, // Средства зарезервированы, ожидание кода подтверждения от пользователя

  // Промежуточные стадии (опционально, для более детального отслеживания)
  PROCESSING_RESERVATION, // Идет процесс резервирования средств
  PROCESSING_SBP, // Идет взаимодействие с СБП-адаптером
  PROCESSING_FUNDS, // Идет процесс списания/зачисления средств (после подтверждения)
  PROCESSING_EIS, // Идет взаимодействие с EIS (отправка чека)

  // Финальные успешные стадии
  SUCCESSFUL, // Перевод успешно завершен

  // Финальные стадии с ошибками/отказом
  FAILED, // Общий статус неудачи (конкретная причина в другом поле)
  RESERVATION_FAILED, // Не удалось зарезервировать средства (например, недостаточно средств)
  CONFIRMATION_FAILED, // Ошибка подтверждения (неверный код, превышено кол-во попыток)
  SBP_ERROR, // Ошибка при взаимодействии с СБП-адаптером
  FUNDS_TRANSFER_FAILED, // Ошибка при фактическом списании/зачислении средств
  EIS_ERROR, // Ошибка при взаимодействии с EIS
  LIMIT_EXCEEDED, // Превышен лимит на переводы
  INVALID_RECIPIENT, // Некорректный получатель или банк получателя
  TIMEOUT, // Истек таймаут ожидания (например, подтверждения)
  CANCELLED, // Перевод отменен (например, пользователем до завершения)
}
